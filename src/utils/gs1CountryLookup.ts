export function getCountryFromBarcode(barcode: string): string | null {
  if (!barcode || barcode.length < 3) return null;

  const prefix = parseInt(barcode.substring(0, 3), 10);
  if (isNaN(prefix)) return null;

  const match = GS1_RANGES.find((r) => prefix >= r.start && prefix <= r.end);
  return match?.country ?? null;
}
